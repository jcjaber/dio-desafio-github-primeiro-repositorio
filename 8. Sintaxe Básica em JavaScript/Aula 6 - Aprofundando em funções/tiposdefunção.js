//TIPOS DE FUNÇÃO

//declarativas
function example() {
  console.log('Exemplo de função declarativa')
}

example();

// expressão de função
// com nomeação
var funcao = function() {
  console.log('Exemplo de função de expressão')
}

funcao();

//arrow function
var arrowFunction = () =>{
  console.log('Exemplo de arrow function')
}

arrowFunction();
